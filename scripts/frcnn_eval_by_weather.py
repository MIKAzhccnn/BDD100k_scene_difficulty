#!/usr/bin/env python3
# -*- coding: utf-8 -*-
import argparse, json, subprocess, time
from pathlib import Path
import yaml
import numpy as np
import pandas as pd
from PIL import Image

import torch, torchvision
from torchvision.models.detection.faster_rcnn import FastRCNNPredictor
from torchvision.transforms import functional as F
from torch.utils.data import DataLoader

# ---------- COCO dataset ----------
from pycocotools.coco import COCO
from pycocotools.cocoeval import COCOeval

class CocoDet(torch.utils.data.Dataset):
    def __init__(self, ann_file, transforms=None):
        self.coco = COCO(ann_file)
        self.ids = list(self.coco.imgs.keys())
        self.transforms = transforms
    def __getitem__(self, idx):
        img_id = self.ids[idx]
        info = self.coco.loadImgs([img_id])[0]
        img = Image.open(info["file_name"]).convert("RGB")
        ann_ids = self.coco.getAnnIds(imgIds=[img_id])
        anns = self.coco.loadAnns(ann_ids)
        boxes, labels, areas, crowds = [], [], [], []
        for a in anns:
            x,y,w,h = a["bbox"]
            if w<=0 or h<=0: continue
            boxes.append([x,y,x+w,y+h])
            labels.append(int(a["category_id"]))
            areas.append(float(a.get("area", w*h)))
            crowds.append(int(a.get("iscrowd", 0)))
        tgt = {"boxes": torch.as_tensor(boxes, dtype=torch.float32),
               "labels": torch.as_tensor(labels, dtype=torch.int64),
               "image_id": torch.tensor([img_id]),
               "area": torch.as_tensor(areas, dtype=torch.float32),
               "iscrowd": torch.as_tensor(crowds, dtype=torch.int64)}
        if self.transforms: img = self.transforms(img)
        return img, tgt
    def __len__(self): return len(self.ids)

def collate_fn(batch):
    imgs, tars = list(zip(*batch))
    return list(imgs), list(tars)

# ---------- Eval & latency ----------
@torch.no_grad()
def evaluate_coco(model, ann_file, device, num_workers=4, score_thresh=0.001):
    model.eval()
    ds = CocoDet(ann_file, transforms=F.to_tensor)
    dl = DataLoader(ds, batch_size=1, shuffle=False, num_workers=num_workers, collate_fn=collate_fn)

    coco = ds.coco
    coco.dataset.setdefault('info', {'description':'generated by yolo2coco', 'version':'1.0'})
    coco.dataset.setdefault('licenses', [])

    img_ids = ds.ids
    results = []
    for images, targets in dl:
        images = [img.to(device) for img in images]
        outs = model(images)
        for out, tgt in zip(outs, targets):
            img_id = int(tgt["image_id"].item())
            boxes = out["boxes"].detach().cpu().numpy()
            scores = out["scores"].detach().cpu().numpy()
            labels = out["labels"].detach().cpu().numpy()
            keep = scores >= score_thresh
            boxes, scores, labels = boxes[keep], scores[keep], labels[keep]
            for b, s, c in zip(boxes, scores, labels):
                x1,y1,x2,y2 = b.tolist()
                results.append({"image_id": img_id, "category_id": int(c),
                                "bbox": [float(x1), float(y1), float(x2-x1), float(y2-y1)],
                                "score": float(s)})
    if not results:
        return {"mAP50_95": 0.0, "mAP50": 0.0}

    cocoDt = coco.loadRes(results)
    ev = COCOeval(coco, cocoDt, "bbox")
    ev.params.imgIds = img_ids
    ev.evaluate(); ev.accumulate(); ev.summarize()
    return {"mAP50_95": float(ev.stats[0]), "mAP50": float(ev.stats[1])}

@torch.no_grad()
def measure_latency(model, ann_file, device, runs=200):
    ds = CocoDet(ann_file, transforms=F.to_tensor)
    ids = ds.ids[:runs] if runs < len(ds) else ds.ids
    times = []
    model.eval()
    for img_id in ids:
        info = ds.coco.loadImgs([img_id])[0]
        img = F.to_tensor(Image.open(info["file_name"]).convert("RGB")).to(device)
        if torch.cuda.is_available(): torch.cuda.synchronize()
        t0 = time.time(); _ = model([img])
        if torch.cuda.is_available(): torch.cuda.synchronize()
        times.append((time.time()-t0)*1000.0)
    if not times: return {"p50_ms": float("nan"), "p95_ms": float("nan")}
    t = np.array(times, dtype=np.float64)
    return {"p50_ms": float(np.percentile(t,50)), "p95_ms": float(np.percentile(t,95))}

# ---------- helpers ----------
def yolo_to_coco(images_dir: Path, labels_dir: Path, names_yaml: Path, out_json: Path, yolo2coco_script: Path):
    out_json.parent.mkdir(parents=True, exist_ok=True)
    if out_json.exists(): return
    subprocess.check_call([
        "python", str(yolo2coco_script),
        "--images_dir", str(images_dir),
        "--labels_dir", str(labels_dir),
        "--names_yaml", str(names_yaml),
        "--out_json", str(out_json),
    ])

def resolve_val_dirs(yaml_path: Path):
    y = yaml.safe_load(yaml_path.read_text(encoding="utf-8")) or {}
    base = Path(y.get("path","."))
    val = y.get("val")
    if val is None:
        raise KeyError(f"No 'val' in {yaml_path}")
    val_dir = (base/val) if not Path(val).is_absolute() else Path(val)
    # 如果是相对 yaml 的相对路径，拼起来
    if not val_dir.exists():
        val_dir = (yaml_path.parent / val_dir).resolve()
    # labels 目录：把路径中的 images 替换成 labels，若不存在则回退到同层级
    s = str(val_dir)
    if "/images/" in s:
        labels_dir = Path(s.replace("/images/","/labels/"))
    else:
        labels_dir = val_dir.parent.parent / "labels" / val_dir.name
    return val_dir, labels_dir

def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--checkpoint", required=True, help="e.g., results/frcnn_all_fp32_bs2acc2/best.pth")
    ap.add_argument("--names_yaml", default="yamls/bdd_all.yaml")
    ap.add_argument("--yaml_dir", default="yamls")
    ap.add_argument("--yolo2coco_script", default="scripts/yolo2coco.py")
    ap.add_argument("--out_csv", default="results/frcnn_by_weather.csv")
    ap.add_argument("--tmp_dir", default="results/frcnn_all/annotations_weather")
    ap.add_argument("--latency_runs", type=int, default=200)
    ap.add_argument("--min_size", type=int, default=640)   # 与训练保持一致更公平
    ap.add_argument("--max_size", type=int, default=1024)
    args = ap.parse_args()

    device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
    all_names = yaml.safe_load(Path(args.names_yaml).read_text(encoding="utf-8"))["names"]
    num_classes = len(all_names) + 1

    # 构建模型并加载权重
    model = torchvision.models.detection.fasterrcnn_resnet50_fpn_v2(weights=None)
    in_feats = model.roi_heads.box_predictor.cls_score.in_features
    model.roi_heads.box_predictor = FastRCNNPredictor(in_feats, num_classes)
    model.transform.min_size = (args.min_size,)
    model.transform.max_size = args.max_size
    ckpt = torch.load(args.checkpoint, map_location="cpu")
    model.load_state_dict(ckpt["model"])
    model.to(device).eval()

    weathers = ["clear","partly_cloudy","overcast","rainy","snowy","foggy"]
    rows = []
    tmp = Path(args.tmp_dir); tmp.mkdir(parents=True, exist_ok=True)

    for w in weathers:
        yp = Path(args.yaml_dir) / f"bdd_{w}.yaml"
        if not yp.exists():
            print(f"[Skip] yaml not found: {yp}"); continue
        val_imgs, val_labels = resolve_val_dirs(yp)
        if not val_imgs.exists():
            print(f"[ERR] val images not found: {val_imgs}"); continue
        if not val_labels.exists():
            print(f"[ERR] val labels not found: {val_labels}"); continue

        out_json = tmp / f"{w}_val.json"
        yolo_to_coco(val_imgs, val_labels, Path(args.names_yaml), out_json, Path(args.yolo2coco_script))

        m = evaluate_coco(model, str(out_json), device=device)
        lat = measure_latency(model, str(out_json), device=device, runs=args.latency_runs)
        print(f"[{w}] mAP50_95={m['mAP50_95']:.4f}  mAP50={m['mAP50']:.4f}  p50={lat['p50_ms']:.2f}ms p95={lat['p95_ms']:.2f}ms")

        rows.append({"weather": w, "model": "frcnn_all",
                     "mAP50": m["mAP50"], "mAP50_95": m["mAP50_95"],
                     "p50_ms": lat["p50_ms"], "p95_ms": lat["p95_ms"]})

    out_csv = Path(args.out_csv); out_csv.parent.mkdir(parents=True, exist_ok=True)
    pd.DataFrame(rows).to_csv(out_csv, index=False)
    print(f"[OK] per-weather metrics -> {out_csv}")

if __name__ == "__main__":
    main()
